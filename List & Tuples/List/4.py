# list Methods
list = [2,1,3]
list.append(4) # adds one elements at the end
print(list)

list.sort() # sorts in ascending order
print()
print(list)

list.sort(reverse = True) # sorts in descending order
print()
print(list)

list.reverse() # reverses list
print()
print(list)

list.insert(0,0) # syntax = list.insert(index,element)
print()
print(list)

list.remove(1) # removes first occurancs of element
print()
print(list)

list.pop(2) # removes element at index
print()
print(list)