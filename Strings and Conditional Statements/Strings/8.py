# String Functions
str = "i am a coder."
print(str.endswith("er.")) # returns true if string ends with substr
print(str.capitalize()) # capitalise 1st char
print(str.replace('a','m')) #replaces all occurances of old with new
print(str.find("am")) # returns 1st index of 1st occurrence
print(str.count("m")) # counts the occurrence of substr in sting